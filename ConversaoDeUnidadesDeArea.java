public class ConversaoDeUnidadesDeArea {

    public static double MetrosParaPes (double metros) {
        return metros * 10.76;
    }

    public static double PesParaCentrimetros (double pes) {
        return pes * 929;
    }

    public static double MilhaParaAcres (double milhas) {
        return milhas * 640;
    }

    public static double AcresParaPes (double acres) {
        return acres * 43.560;
    }
}
